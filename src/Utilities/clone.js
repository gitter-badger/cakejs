//CakeJS.Utilities.clone

export default function clone(obj){
	var copy;
    if (null == obj || "object" != typeof obj) 
		return obj;
    if (obj instanceof Date) {
        copy = new Date();
        copy.setTime(obj.getTime());
        return copy;
    }
    if (obj instanceof Array) {
        copy = [];
        for (var i = 0, len = obj.length; i < len; i++) {
            copy[i] = clone(obj[i]);
        }
        return copy;
    }
	return obj;
}